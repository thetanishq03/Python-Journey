#STRING METHODS
#Uppercase
name = "tanishq"
print(name.upper())
#Lowercase
name = "PYTHON"
print(name.lower())
#Titlecase
name = "tanishq tamhankar"
print(name.title())
#Replace
sentence = "I like Java"
print(sentence.replace("Java", "Python"))
#Count
word = "Banana"
print(word.count("a"))
#Find
word = "Python"
print(word.find("h"))
#Check if something exists(in)
name = "Tanishq"
print("n" in name)
print("z" in name)